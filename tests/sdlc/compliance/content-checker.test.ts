// ============================================================================
// Stage Content Check (with temp dirs)
// ============================================================================

let tempDir: string;

beforeEach(() => {
  tempDir = fs.mkdtempSync(path.join(os.tmpdir(), "endiorbot-l2-test-"));
});

afterEach(() => {
  if (tempDir && fs.existsSync(tempDir)) {
    fs.rmSync(tempDir, { recursive: true, force: true });
  }
});

describe("checkStageContent", () => {
  it("should detect placeholder-only stage (BUG-011 scenario)", () => {
    // Create a stage directory with only a template README
    const stagePath = path.join(tempDir, "docs", "05-test");
    fs.mkdirSync(stagePath, { recursive: true });
    fs.writeFileSync(
      path.join(stagePath, "README.md"),
      [
        "# 05-test - Test",
        "",
        "## Contents",
        "<!-- Add stage-specific documentation here -->",
        "- [ ] TODO: Add test artifacts",
        "",
        "| TBD | Pending | TBD |",
        "",
        "*Generated by EndiorBot - SDLC Framework v6.1.1*",
      ].join("\n"),
    );

    const result = checkStageContent(tempDir, "05-test");
    expect(result.hasRealContent).toBe(false);
    expect(result.placeholdersFound.length).toBeGreaterThan(0);
    expect(result.contentScore).toBeLessThan(50);
  });

  it("should detect stage with real content", () => {
    const stagePath = path.join(tempDir, "docs", "00-foundation");
    fs.mkdirSync(stagePath, { recursive: true });

    // Create a real problem-statement.md
    const lines: string[] = [];
    lines.push("# Problem Statement");
    lines.push("");
    lines.push("## Overview");
    for (let i = 0; i < 30; i++) {
      lines.push(`Real content line ${i + 1} describing the problem.`);
    }
    fs.writeFileSync(path.join(stagePath, "problem-statement.md"), lines.join("\n"));

    // Create business-case.md
    const bLines: string[] = [];
    bLines.push("# Business Case");
    bLines.push("");
    for (let i = 0; i < 20; i++) {
      bLines.push(`Business justification line ${i + 1}.`);
    }
    fs.writeFileSync(path.join(stagePath, "business-case.md"), bLines.join("\n"));

    const result = checkStageContent(tempDir, "00-foundation");
    expect(result.hasRealContent).toBe(true);
    expect(result.placeholdersFound.length).toBe(0);
    expect(result.contentScore).toBeGreaterThanOrEqual(75);
    expect(result.artifactsFound).toContain("problem-statement.md");
    expect(result.artifactsFound).toContain("business-case.md");
  });

  it("should handle non-existent stage directory", () => {
    const result = checkStageContent(tempDir, "99-nonexistent");
    expect(result.hasRealContent).toBe(false);
    expect(result.contentScore).toBe(0);
  });
});

// ============================================================================
// Full L2 Compliance Check
// ============================================================================

describe("checkL2Compliance", () => {
  it("should score low for placeholder-only project (BUG-011 verification)", () => {
    // Create standard stage structure with all placeholders
    const stages = ["00-foundation", "01-planning", "02-design", "04-build", "05-test"];
    for (const stage of stages) {
      const stagePath = path.join(tempDir, "docs", stage);
      fs.mkdirSync(stagePath, { recursive: true });
      fs.writeFileSync(
        path.join(stagePath, "README.md"),
        [
          `# ${stage}`,
          "<!-- Add stage-specific documentation here -->",
          "- [ ] TODO: Add artifacts",
          "| TBD | Pending | TBD |",
          "*Generated by EndiorBot*",
        ].join("\n"),
      );
    }

    const result = checkL2Compliance(tempDir, stages, "STANDARD");
    expect(result.score).toBeLessThan(50);
    expect(result.issues.length).toBeGreaterThan(0);
    expect(result.issues.some((i) => i.type === "placeholder")).toBe(true);
  });

  it("should score high for project with real content", () => {
    // Create 00-foundation with real content
    const foundationPath = path.join(tempDir, "docs", "00-foundation");
    fs.mkdirSync(foundationPath, { recursive: true });

    const lines: string[] = ["# Problem Statement", ""];
    for (let i = 0; i < 30; i++) lines.push(`Content line ${i + 1}.`);
    fs.writeFileSync(path.join(foundationPath, "problem-statement.md"), lines.join("\n"));
    fs.writeFileSync(path.join(foundationPath, "business-case.md"), lines.join("\n"));

    const result = checkL2Compliance(tempDir, ["00-foundation"], "STANDARD");
    expect(result.score).toBe(100);
    expect(result.passedChecks).toBe(1);
  });
});
