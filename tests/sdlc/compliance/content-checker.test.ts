import {
  isPlaceholderLine,
  countPlaceholders,
  countContentLines,
  checkStageContent,
  checkL2Compliance,
  STAGE_CONTENT_REQUIREMENTS,
} from "../../../src/sdlc/compliance/content-checker.js";

// ============================================================================
// Placeholder Detection
// ============================================================================

describe("isPlaceholderLine", () => {
  it("should detect TODO: Add patterns", () => {
    expect(isPlaceholderLine("- [ ] TODO: Add test artifacts")).toBe(true);
    expect(isPlaceholderLine("TODO: Add planning artifacts")).toBe(true);
  });

  it("should detect TBD markers", () => {
    expect(isPlaceholderLine("| TBD | Pending | TBD |")).toBe(true);
  });

  it("should detect HTML placeholder comments", () => {
    expect(isPlaceholderLine("<!-- Add stage-specific documentation here -->")).toBe(true);
  });

  it("should detect Generated by EndiorBot marker", () => {
    expect(isPlaceholderLine("*Generated by EndiorBot - SDLC Framework v6.1.1*")).toBe(true);
  });

  it("should NOT flag real content", () => {
    expect(isPlaceholderLine("## Problem Statement")).toBe(false);
    expect(isPlaceholderLine("The system shall provide authentication")).toBe(false);
    expect(isPlaceholderLine("- User login with email/password")).toBe(false);
  });
});

describe("countPlaceholders", () => {
  it("should count placeholder lines in template content", () => {
    const template = [
      "# 05-test - Test",
      "",
      "## Contents",
      "<!-- Add stage-specific documentation here -->",
      "- [ ] TODO: Add test artifacts",
      "",
      "| TBD | Pending | TBD |",
      "",
      "*Generated by EndiorBot - SDLC Framework v6.1.1*",
    ].join("\n");

    const placeholders = countPlaceholders(template);
    expect(placeholders.length).toBe(4);
  });

  it("should return empty for real content", () => {
    const content = [
      "# Problem Statement",
      "",
      "## Overview",
      "The application needs to handle user authentication.",
      "",
      "## Requirements",
      "- Login with email/password",
      "- OAuth2 integration",
    ].join("\n");

    expect(countPlaceholders(content)).toEqual([]);
  });
});

// ============================================================================
// Content Line Counting
// ============================================================================

describe("countContentLines", () => {
  it("should count non-empty, non-placeholder lines", () => {
    const content = [
      "# Title",
      "",
      "Some real content here.",
      "More content.",
      "---",
      "- [ ] TODO: Add artifacts",
      "Final line.",
    ].join("\n");

    // "# Title" (1) + "Some real content" (2) + "More content" (3) + "Final line" (4)
    // Excludes: empty, ---, TODO placeholder
    expect(countContentLines(content)).toBe(4);
  });

  it("should return 0 for empty content", () => {
    expect(countContentLines("")).toBe(0);
    expect(countContentLines("\n\n\n")).toBe(0);
  });

  it("should return 0 for placeholder-only content", () => {
    const content = [
      "---",
      "- [ ] TODO: Add artifacts",
      "*Generated by EndiorBot - SDLC Framework v6.1.1*",
      "---",
    ].join("\n");

    expect(countContentLines(content)).toBe(0);
  });
});

