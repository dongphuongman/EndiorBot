// ============================================================================
// Types
// ============================================================================

export interface ContentIssue {
  type: "placeholder" | "insufficient_content" | "missing_artifact";
  severity: "error" | "warning";
  message: string;
  file?: string;
  stage?: string;
}

export interface L2Result {
  score: number;
  totalChecks: number;
  passedChecks: number;
  issues: ContentIssue[];
  stageResults: StageContentResult[];
}

export interface StageContentResult {
  stage: string;
  hasRealContent: boolean;
  contentScore: number;
  totalLines: number;
  placeholdersFound: string[];
  artifactsFound: string[];
  artifactsMissing: string[];
}

// ============================================================================
// Placeholder Detection Patterns
// ============================================================================

const PLACEHOLDER_PATTERNS: RegExp[] = [
  /TODO:\s*Add/i,
  /\bTBD\b/,
  /<!--\s*Add\s+stage-specific/i,
  /\bPending\b.*\bTBD\b/,
  /\[\s*\]\s*TODO/,
  /Generated by EndiorBot/i,
];

/**
 * Check if a line matches any placeholder pattern.
 */
export function isPlaceholderLine(line: string): boolean {
  return PLACEHOLDER_PATTERNS.some((pattern) => pattern.test(line));
}

/**
 * Count placeholder lines in content.
 */
export function countPlaceholders(content: string): string[] {
  const lines = content.split("\n");
  const found: string[] = [];
  for (const line of lines) {
    const trimmed = line.trim();
    if (trimmed && isPlaceholderLine(trimmed)) {
      found.push(trimmed);
    }
  }
  return found;
}

