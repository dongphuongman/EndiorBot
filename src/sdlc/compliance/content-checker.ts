// ============================================================================
// Content Analysis
// ============================================================================

/**
 * Count meaningful content lines (non-empty, non-placeholder, non-header-only).
 */
export function countContentLines(content: string): number {
  const lines = content.split("\n");
  let count = 0;
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    if (trimmed.startsWith("---")) continue;
    if (isPlaceholderLine(trimmed)) continue;
    count++;
  }
  return count;
}

/**
 * Get all markdown/yaml files in a stage directory (recursive, 2 levels).
 */
function getStageFiles(stagePath: string): string[] {
  const files: string[] = [];
  if (!existsSync(stagePath)) return files;

  try {
    const entries = readdirSync(stagePath, { withFileTypes: true });
    for (const entry of entries) {
      const entryPath = join(stagePath, entry.name);
      if (entry.isFile() && /\.(md|yaml|yml|json)$/i.test(entry.name)) {
        files.push(entry.name);
      } else if (entry.isDirectory()) {
        // Go one level deeper
        try {
          const subEntries = readdirSync(entryPath, { withFileTypes: true });
          for (const sub of subEntries) {
            if (sub.isFile() && /\.(md|yaml|yml|json)$/i.test(sub.name)) {
              files.push(join(entry.name, sub.name));
            }
          }
        } catch {
          // Permission or access error
        }
      }
    }
  } catch {
    // Permission or access error
  }
  return files;
}

/**
 * Check if a required artifact exists in the stage files.
 * Supports simple wildcard patterns (* at end).
 */
function artifactExists(required: string, stageFiles: string[]): boolean {
  if (required.includes("*")) {
    const prefix = required.replace(/\*.*$/, "");
    return stageFiles.some((f) => f.startsWith(prefix));
  }
  return stageFiles.some((f) => f === required || f.endsWith(`/${required}`));
}

// ============================================================================
// Stage Content Check
// ============================================================================

/**
 * Check content quality for a single stage.
 */
export function checkStageContent(
  projectPath: string,
  stage: string,
): StageContentResult {
  const stagePath = join(projectPath, "docs", stage);
  const requirements = STAGE_CONTENT_REQUIREMENTS[stage];

  const result: StageContentResult = {
    stage,
    hasRealContent: false,
    contentScore: 0,
    totalLines: 0,
    placeholdersFound: [],
    artifactsFound: [],
    artifactsMissing: [],
  };

  if (!existsSync(stagePath)) return result;

  // Get all files in stage
  const stageFiles = getStageFiles(stagePath);

  // Read and analyze all content
  let totalContentLines = 0;
  const allPlaceholders: string[] = [];

  for (const file of stageFiles) {
    const filePath = join(stagePath, file);
    try {
      const stat = statSync(filePath);
      if (!stat.isFile()) continue;
      const content = readFileSync(filePath, "utf-8");
      totalContentLines += countContentLines(content);
      allPlaceholders.push(...countPlaceholders(content));
    } catch {
      // Skip unreadable files
    }
  }

  result.totalLines = totalContentLines;
  result.placeholdersFound = allPlaceholders;

  if (!requirements) {
    // Unknown stage — score based on content alone
    result.hasRealContent = totalContentLines > 10 && allPlaceholders.length === 0;
    result.contentScore = totalContentLines > 10 ? 100 : 0;
    return result;
  }

  // Check required artifacts
  for (const artifact of requirements.requiredArtifacts) {
    if (artifactExists(artifact, stageFiles)) {
      result.artifactsFound.push(artifact);
    } else {
      result.artifactsMissing.push(artifact);
    }
  }

  // Check required subdirs
  if (requirements.requiredSubdirs) {
    for (const subdir of requirements.requiredSubdirs) {
      const subdirPath = join(stagePath, subdir);
      if (existsSync(subdirPath)) {
        result.artifactsFound.push(`${subdir}/`);
      } else {
        result.artifactsMissing.push(`${subdir}/`);
      }
    }
  }

  // Calculate score (4 factors, each 25%)
  const factors: number[] = [];

  // Factor 1: Content volume (min lines met?)
  const lineFactor = Math.min(totalContentLines / requirements.minContentLines, 1.0);
  factors.push(lineFactor);

  // Factor 2: No placeholders
  const placeholderRatio = stageFiles.length > 0
    ? Math.max(0, 1 - allPlaceholders.length / Math.max(stageFiles.length, 1))
    : 0;
  factors.push(placeholderRatio);

  // Factor 3: Required artifacts present
  const requiredTotal = requirements.requiredArtifacts.length + (requirements.requiredSubdirs?.length ?? 0);
  const artifactFactor = requiredTotal > 0
    ? (requiredTotal - result.artifactsMissing.length) / requiredTotal
    : 1.0; // No required artifacts = full score
  factors.push(artifactFactor);

  // Factor 4: File count beyond just README.md
  const meaningfulFiles = stageFiles.filter((f) => f.toLowerCase() !== "readme.md");
  const fileFactor = meaningfulFiles.length > 0 ? 1.0 : 0.0;
  factors.push(fileFactor);

  result.contentScore = Math.round(
    factors.reduce((sum, f) => sum + f, 0) / factors.length * 100,
  );
  result.hasRealContent = result.contentScore >= 50;

  return result;
}

// ============================================================================
// Full L2 Check
// ============================================================================

/**
 * Run L2 content quality checks across all stages.
 */
export function checkL2Compliance(
  projectPath: string,
  stages: string[],
  _tier: ProjectTier,
): L2Result {
  const issues: ContentIssue[] = [];
  const stageResults: StageContentResult[] = [];

  for (const stage of stages) {
    const result = checkStageContent(projectPath, stage);
    stageResults.push(result);

    // Report placeholders
    if (result.placeholdersFound.length > 0) {
      issues.push({
        type: "placeholder",
        severity: "warning",
        message: `Stage ${stage}: ${result.placeholdersFound.length} placeholder(s) found`,
        stage,
      });
    }

    // Report insufficient content
    const requirements = STAGE_CONTENT_REQUIREMENTS[stage];
    if (requirements && result.totalLines < requirements.minContentLines) {
      issues.push({
        type: "insufficient_content",
        severity: "warning",
        message: `Stage ${stage}: ${result.totalLines} content lines (minimum: ${requirements.minContentLines})`,
        stage,
      });
    }

    // Report missing artifacts
    for (const missing of result.artifactsMissing) {
      issues.push({
        type: "missing_artifact",
        severity: "warning",
        message: `Stage ${stage}: missing required artifact '${missing}'`,
        stage,
        file: missing,
      });
    }
  }

  // Calculate overall L2 score
  const totalChecks = stageResults.length;
  const passedChecks = stageResults.filter((r) => r.hasRealContent).length;
  const score = totalChecks > 0 ? Math.round((passedChecks / totalChecks) * 100) : 0;

  return {
    score,
    totalChecks,
    passedChecks,
    issues,
    stageResults,
  };
}
