/**
 * Get 4 Non-Negotiable Invariants section.
 */
function getInvariantsSection(): string {
  return `<!-- ENDIORBOT:MANAGED:START -->
## 4 Non-Negotiable Invariants

\`\`\`
1. THIN CLIENT PATTERN
   Claude Code commands = wrappers that call ./endiorbot.mjs
   NO business logic in .md files
   Gate checks, vibecoding, multi-model → all in EndiorBot core

2. STDIN JSON FOR HOOKS
   Hooks receive JSON via stdin, NOT positional arguments
   Always parse with jq: cat /dev/stdin | jq -r '.tool_name'
   Test: echo '{"tool_name":"Edit","file_path":"test.ts"}' | ./hook.sh

3. ENDIORBOT SOUL = GOVERNANCE, CLAUDE CODE = EXECUTION
   EndiorBot SOUL decides WHAT to build (PM, requirements, gates)
   Claude Code executes HOW to build (Architect, Coder, Reviewer)
   No PM agent in Claude Code (prevents orchestration conflict)

4. DEFAULT MODEL = SONNET
   Opus only for explicit architecture decisions
   Commands use model: sonnet unless specified
   Budget guard: track /cost regularly
\`\`\`
<!-- ENDIORBOT:MANAGED:END -->`;
}

/**
 * Get tier-specific section.
 */
function getTierSection(tier: ProjectTier): string {
  const sections: Record<ProjectTier, string> = {
    LITE: `## Tier: LITE

**Features:**
- 2 AI agents (assistant, coder)
- 4 lifecycle stages (foundation, planning, design, build)
- Basic gate requirements
- Solo developer workflow`,

    STANDARD: `## Tier: STANDARD

**Features:**
- 5+ AI agents (assistant, pm, pjm, coder, reviewer)
- 7 lifecycle stages
- Sprint gates (G-Sprint, G-Sprint-Close)
- Team collaboration support
- Soft enforcement mode`,

    PROFESSIONAL: `## Tier: PROFESSIONAL

**Features:**
- 10+ AI agents including researcher, architect, tester
- 10 lifecycle stages
- Full gate pipeline (G0.1 → G4)
- Hard enforcement mode
- Code review requirements`,

    ENTERPRISE: `## Tier: ENTERPRISE

**Features:**
- 12 AI agents including DevOps
- Full 11-stage lifecycle
- Executive gates (G0.1, G4)
- Audit trail required
- Maximum governance`,
  };

  return sections[tier];
}

