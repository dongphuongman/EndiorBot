/**
 * Generate CLAUDE.md content.
 */
export function generateClaudeMd(project: ProjectConfig): string {
  const tierSection = getTierSection(project.tier);
  const commandsSection = getCommandsSection();
  const invariantsSection = getInvariantsSection();

  return `# CLAUDE.md - ${project.name}

## Overview

${project.description || `${project.name} project managed with EndiorBot SDLC Framework.`}

**Tier:** ${project.tier}
**Framework:** SDLC 6.1.1

---

${invariantsSection}

---

## Configuration Files

| File | Purpose |
|------|---------|
| \`.sdlc-config.json\` | SDLC framework configuration |
| \`IDENTITY.md\` | Project identity and capabilities |
| \`AGENTS.md\` | AI agent guidelines |
| \`CLAUDE.md\` | Claude-specific integration (this file) |

---

${tierSection}

---

${commandsSection}

---

## Code Style

### TypeScript
- Use strict mode
- Explicit types (no \`any\`)
- Prefer \`const\` over \`let\`
- Use template literals
- Document public APIs with JSDoc

### Commit Messages

Follow conventional commits:
\`\`\`
feat(scope): add feature description
fix(scope): fix bug description
docs(scope): update documentation
refactor(scope): refactor code
test(scope): add tests
\`\`\`

---

## SDLC Integration

### Gate Awareness
Before major changes, check gate requirements:
\`\`\`bash
./endiorbot.mjs gate status
./endiorbot.mjs gate recommend G2
\`\`\`

### Quality Assurance
Monitor vibecoding index to maintain code quality:
\`\`\`bash
./endiorbot.mjs vibecoding check
\`\`\`

Target: Vibecoding Index < 40 (Green zone)

---

*CLAUDE.md generated by EndiorBot*
*SDLC Framework v${project.frameworkVersion}*
`;
}

